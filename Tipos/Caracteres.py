animal="Pinguino, mamifero, ave"
print(animal.count("a")) # Cuenta cuantas veces aparece el caracter
print(animal.find("m")) # Encuentra la posición del caracter
print(animal.replace("Pinguino","Gato")) # Reemplaza un caracter por otro
print(animal.upper()) # Pasa a mayúsculas
print(animal.lower()) # Pasa a minúsculas
print(animal.capitalize()) # Pasa a mayúscula la primera letra
print(animal.title()) # Pasa a mayúscula la primera letra de cada palabra
print(animal.strip()) # Elimina espacios al inicio y al final
print(animal.startswith("P")) # Devuelve True si empieza por el caracter
print(animal.endswith("e")) # Devuelve True si termina por el caracter
print(animal.split(",")) # Devuelve una lista separada por el caracter