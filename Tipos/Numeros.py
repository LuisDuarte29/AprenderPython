import math
print(4/2)
print(4//2) # División entera
print(4%2) # Módulo
print(4**2) # Potencia
print(round(1.5)) # Redondea al número entero más cercano
print(abs(-4)) # Valor absoluto
print(pow(4,2)) # Potencia
print(max(4,2)) # Devuelve el número mayor

print(math.ceil(1.2)) # Redondea al número entero superior
print(math.floor(1.8)) # Redondea al número entero inferior
print(math.sqrt(16)) # Raíz cuadradabc
print(math.isnan(5)) # Devuelve True si no es un número
print(math.pow(100)) # Potencia